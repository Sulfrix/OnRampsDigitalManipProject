function prettyPrintCategoryName(cat) {
    switch (cat) {
        case "barrel":
            return "Barrels";
        case "receiver":
            return "Receivers";
        case "stock":
            return "Stocks";
        case "sight":
            return "Sights";
        case "attachment":
            return "Attachments";
        case "muzzle":
            return "Muzzle";
        case "pistol":
            return "Pistols";
    }
}